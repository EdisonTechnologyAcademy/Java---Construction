package com.edison.prac02less10;

public class ChangeDataTypeOfArg {
	public int average(int a, int b, int c) {
		return (a + b + c) / 3;
	}

	public double average(double a, double b, double c) {
		return (a + b + c) / 3;
	}

	public long average(long a, long b, long c) {
		return (a + b + c) / 3;
	}
}
